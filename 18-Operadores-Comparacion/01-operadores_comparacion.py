# =====================================
# Operadores Booleanos de Comparación
# (también: Operadores Relacionales)

# - Aplicado a valores numéricos

'''
Operador |  Definición
-------------------------------
   >     |  Mayor que
   <     |  Menor que
   >=    |  Mayor o igual que
   <=    |  Menor o igual que
   ==    |  Igual que
   !=    |  Diferente de
'''
# =====================================



print('\n1) Mayor que (>)')



print('\n2) Menor que (<)')



print('\n3) Mayor o Igual que (>=)')


#! El orden es importante



print('\n4) Menor o Igual que (<=)')



print('\n5) Igual que (==)')



print('\n6) Distinto de (!=)')
