# - Es la identificación de pares e impares

# - El fundamento se basa en el módulo
# EJ:

#   4  |__2
#  -4    2
#*  0

# - El módulo de cualquier número par evaluado en 2 es 0
# - Y de cualquier número impar es diferente de 0 
